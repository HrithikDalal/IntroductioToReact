const PI = 3.14259;

function doublePi() {
  return PI * 2;
}

function triplePi() {
  return PI * 3;
}

function add(n1, n2) {
  return n1 + n2;
}

function multiply(n1, n2) {
  return n1 * n2;
}

function subtract(n1, n2) {
  return n1 - n2;
}

function divide(n1, n2) {
  return n1 / n2;
}

export default PI;
export { doublePi, triplePi, add, multiply, subtract, divide };
